# İki sayının toplamını bulma algoritması

# İlk adımda, iki sayıyı saklamak için num1 ve num2 adında iki değişken oluşturuyoruz.
num1 = 10
num2 = 20

# Şimdi, bu iki sayının toplamını hesaplayıp sonucu sum adlı bir değişkene kaydediyoruz.
sum = num1 + num2

# Sonucu ekrana yazdırmak için 'print' fonksiyonunu kullanıyoruz. 
# Bu, kullanıcıya toplam sonucunu gösterir.
print(f'Sum of {num1} and {num2} = {sum}')
